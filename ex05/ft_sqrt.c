int	ft_sqrt(int nb)
{
	int i;

	i = 1;
	while (i * i < nb && i <= 46340)
		i++;
	if (i * i == nb)
		return (i);
	else
		return (0);
}

#include <stdio.h>

int	main()
{
	printf("%d\n", ft_sqrt(35));
	return (0);
}
