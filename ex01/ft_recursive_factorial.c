int	ft_recursive_factorial(int nb)
{
	if (nb == 0)
		return (1);
	if (nb < 0)
		return (0);
	return (nb * ft_recursive_factorial(nb - 1));
}

#include <stdio.h>

int	main()
{
	printf("%d\n", ft_recursive_factorial(-2));
	return (0);
}
